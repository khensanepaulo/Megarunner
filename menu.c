void jogar(int p, int y)
{
  while (jogando == 1) {
    p = p - 5;

    lcd.fillRect(p + 3, 165, 30, 30, BLACK);
    lcd.drawRGBBitmap(p, 165, flor, 30, 30);

    if (p == 0)
    {
      lcd.fillRect(p, 165, 30, 30, BLACK);
      p = 240;
    }


    if (digitalRead(botao1) == LOW) {

      while (y != 70)
      {

        y = y - 8;
      
        lcd.drawRGBBitmap(20, y, mega, 65, 42);
        p = p - 5;

        lcd.fillRect(p + 3, 165, 30, 30, BLACK);
        lcd.drawRGBBitmap(p, 165, flor, 30, 30);

        if (p == 0) {
          lcd.fillRect(p, 165, 30, 30, BLACK);
          p = 240;
        }

        if (y > 140 && p >= 5 && p <= 30) {
          jogando = 0;
        }


      }

      while (y != 150)
      {

        y = y + 8;
        
        lcd.drawRGBBitmap(20, y, mega, 65, 42);
       lcd.fillRect(28, y-4, 50, 8, BLACK);

        p = p - 5;

        lcd.fillRect(p + 3, 165, 30, 30, BLACK);
        lcd.drawRGBBitmap(p, 165, flor, 30, 30);

        if (p == 0) {
          lcd.fillRect(p, 165, 30, 30, BLACK);
          p = 240;
        }

        if (y > 140 && p >= 5 && p <= 30) {
          jogando = 0;
        }

        score = score + 1;

      }
    }

    int rank = EEPROM.read(highscore);

    if (score > rank) {
      EEPROM.write(highscore, score);
    }

    lcd.fillRect(121, 2, 240, 20, BLACK);
    lcd.setTextSize(2);
    lcd.setCursor(120, 2);
    lcd.print("score: ");
    lcd.print(score);


    if (y > 140 && p >= 2 && p <= 40)
    {
      jogando = 0;
    }

    else if (digitalRead(botao1) == HIGH) {
      lcd.drawRGBBitmap(20, 150, mega, 65, 42);
    }
  }
}



